// Parent class
class Animal {
    String name; // instance variable

    Animal(String name) {
        this.name = name;
    }

    void speak() { // non-static method
        System.out.println(name + " makes a sound");
    }
}

// Child class
class Dog extends Animal {

    Dog(String dogName) {
        super(dogName); // pass the correct name to the parent constructor
    }

    @Override
    void speak() { // override the method properly
        System.out.println(name + " barks");
    }
}
